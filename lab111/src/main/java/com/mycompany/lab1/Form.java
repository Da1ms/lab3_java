    private void AddAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddAction

        //
        // Проверка наличия данных в полях заполнения
        //
        if (startPoint.getText() == null || "".equals(startPoint.getText()))
            return;
        if (finishPoint.getText() == null || "".equals(finishPoint.getText()))
            return;
        if (stepPoint.getText() == null || "".equals(stepPoint.getText()) 
                || "0".equals(stepPoint.getText()))
            return;
        
        start = Double.parseDouble(startPoint.getText());
        finish = Double.parseDouble(finishPoint.getText());
        step = Double.parseDouble(stepPoint.getText());
        
        //
        // Ввод данных в таблицу
        //
        DefaultTableModel tbModel = (DefaultTableModel)table.getModel();
        tbModel.insertRow(countElemInTable, new Object[] {stepPoint.getText(), startPoint.getText(), finishPoint.getText()});
        
        stepPoint.setText("");
        startPoint.setText("");
        finishPoint.setText("");

        countElemInTable++;
        
          
    }//GEN-LAST:event_AddAction
 
