    private void ResultAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResultAction
          
        double sum = 0, numForLoop = 0, result = 0;
        
        //
        // Определение начала интегрирования
        //
        if (start < finish)
        {
            sum = start;
            numForLoop = finish;
        }
        else
        {
            MyException("Out of..."); 
        }
        
        //
        // Расчет площадей трапейций, входящих в промежуток интегрирования
        //
        while (sum < numForLoop)
        {
            result += (Math.tan(sum) + Math.tan(sum + step)) * (step / (double)2);
            sum += step;
        }
        
        //
        // Изменение шага, если шаг вышел за промежуток интегрирования
        //
        if (sum != numForLoop)
        {
            step = numForLoop - sum;
            result += (Math.tan(sum) + Math.tan(sum + step)) * (step / (double)2);
        }
        
        DefaultTableModel tbModel = (DefaultTableModel)table.getModel();
        tbModel.setValueAt(result, countElemInTable - 1, 3);
        
        
    }//GEN-LAST:event_ResultAction

